function langToPath(lang){
    return{
        about:      `${lang}/about`,
        homepage:   `${lang}`,
        news:       `${lang}/news`,
        medieval:   `${lang}/medieval`,
        presale:    `${lang}/presale`,
        qa:         `${lang}/qa`,
        qaForm:     `${lang}/qa#form`,
        seminar:    `${lang}/seminar`,
        user:       `#`,
    }
}
export default langToPath
