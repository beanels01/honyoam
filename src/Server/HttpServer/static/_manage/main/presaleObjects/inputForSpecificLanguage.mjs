import quillImageHandler from   '../quillImageHandler.mjs'
let
    formats=[
        'link',
        'image',
    ],
    optionsForConcept={
        placeholder:'概念內文',
        modules:{
            toolbar:{
                container:formats,
                handlers:{image:quillImageHandler},
            },
        },
        formats,
    },
    optionsForEnvironment={
        placeholder:'環境內文',
        modules:{
            toolbar:{
                container:formats,
                handlers:{image:quillImageHandler},
            },
        },
        formats,
    },
    optionsForTraffic={
        placeholder:'交通內文',
        modules:{
            toolbar:{
                container:formats,
                handlers:{image:quillImageHandler},
            },
        },
        formats,
    }
let inputForSpecificLanguage={
    components:{
        quillEditor:VueQuillEditor.quillEditor,
    },
    created(){
        this.checkValue()
    },
    data:()=>({
        optionsForConcept,
        optionsForEnvironment,
        optionsForTraffic,
    }),
    methods:{
        checkValue(){
            if(this.value)
                return
            this.$emit('input',{
                name:'',
                informationTitle:'',
                informationContent:'',
                conceptSummary:'',
                conceptContent:'',
                environmentSummary:'',
                environmentContent:'',
                trafficSummary:'',
                trafficContent:'',
                videoId:'rNsgHMklBW0',
            })
        },
        videoIdInput(e){
            let m=e.target.value.match(/\?v=(.*)/)
            if(m)
                this.value.videoId=m[1]
        }
    },
    props:['value'],
    template:`
        <div v-if=value>
            <p>
                <input
                    placeholder=名稱
                    v-model=value.name
                >
            </p>
            <p>
                <input
                    placeholder=介紹標題
                    v-model=value.informationTitle
                >
            </p>
            <p>
                <textarea
                    placeholder=介紹內容
                    v-model=value.informationContent
                ></textarea>
            </p>
            <p>
                <textarea
                    placeholder=概念概要
                    v-model=value.conceptSummary
                ></textarea>
            </p>
            <p>
                <quillEditor
                    style=width:480px
                    :options=optionsForConcept
                    v-model=value.conceptContent
                ></quillEditor>
            </p>
            <p>
                <textarea
                    placeholder=環境概要
                    v-model=value.environmentSummary
                ></textarea>
            </p>
            <p>
                <quillEditor
                    style=width:480px
                    :options=optionsForEnvironment
                    v-model=value.environmentContent
                ></quillEditor>
            </p>
            <p>
                <textarea
                    placeholder=交通概要
                    v-model=value.trafficSummary
                ></textarea>
            </p>
            <p>
                <quillEditor
                    style=width:480px
                    :options=optionsForTraffic
                    v-model=value.trafficContent
                ></quillEditor>
            </p>
            <p>
                這裡要設定的是 YouTube 的影片 ID。以下兩個欄位，擇一填寫即可：
            </p>
            <p>
                <input
                    placeholder="影片 ID"
                    v-model=value.videoId
                ></textarea>
            </p>
            <p>
                <input
                    placeholder="影片 URL（網址）"
                    :value="'https://www.youtube.com/watch?v='+value.videoId"
                    @input=videoIdInput
                ></textarea>
            </p>
        </div>
    `,
    watch:{
        value(){
            this.checkValue()
        },
    },
}
export default inputForSpecificLanguage
